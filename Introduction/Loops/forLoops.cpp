#include <iostream>

int main() {

    /*
    
    The for Loop:

    This loop keeps iterating for a pre-specified number of interations
    The syntax for a for loop is as follows:

    for(<loop variable>; <loop condition>; <update condition>) {
        // Code Block
    }

    */

   for (int i = 0; i <= 3; i++) {
        std::cout << "Current Iteration Number " << i << "\n";
   }

    return 0;

}